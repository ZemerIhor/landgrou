#!/bin/bash

# Скрипт для финальной очистки польского языка
# Автор: Ассистент Claude
# Дата: $(date)

echo "🚀 Начинаем финальную очистку польского языка..."

# Проверяем, что мы в правильной директории
if [ ! -f "artisan" ]; then
    echo "❌ Ошибка: Файл artisan не найден. Убедитесь, что вы находитесь в корне Laravel проекта."
    exit 1
fi

echo "📁 Текущая директория: $(pwd)"

# Проверяем наличие папки lang/pl
if [ -d "lang/pl" ]; then
    echo "🗑️  Удаляем папку lang/pl/..."
    rm -rf lang/pl/
    echo "✅ Папка lang/pl/ успешно удалена"
else
    echo "ℹ️  Папка lang/pl/ не найдена (возможно, уже удалена)"
fi

# Проверяем, что остались только украинские файлы
echo "📋 Проверяем содержимое lang/:"
ls -la lang/

# Очищаем кеш Laravel
echo "🧹 Очищаем кеш Laravel..."

php artisan config:clear
echo "✅ Кеш конфигурации очищен"

php artisan view:clear  
echo "✅ Кеш представлений очищен"

php artisan cache:clear
echo "✅ Общий кеш очищен"

# Проверяем настройки локали
echo "🌍 Проверяем текущую локаль..."
LOCALE=$(php artisan tinker --execute="echo app()->getLocale();")
echo "Текущая локаль: $LOCALE"

if [ "$LOCALE" = "uk" ]; then
    echo "✅ Локаль правильно настроена на украинский (uk)"
else
    echo "⚠️  Внимание: Локаль не установлена на 'uk'. Текущая: $LOCALE"
fi

echo ""
echo "🎉 МИГРАЦИЯ ЗАВЕРШЕНА УСПЕШНО!"
echo ""
echo "✅ Проверьте:"
echo "   - Сайт загружается без ошибок"
echo "   - Все тексты на украинском языке"
echo "   - Админка работает корректно"
echo "   - Формы показывают украинские сообщения"
echo ""
echo "📝 Рекомендуется создать Git коммит:"
echo "   git add ."
echo "   git commit -m 'Финальная очистка: удаление польских языковых файлов'"
echo ""
echo "🎯 Миграция с польского на украинский язык полностью завершена!"
